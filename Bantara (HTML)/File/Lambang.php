				<td><ul type="circle">
						<li>Tunas buah kelapa digunakan sebagai lambang pramuka karena buah kelapa dalam keadaan tumbuh itu dinamakan "Cikal", dan istilah "Cikal Bakal" di indonesia berarti, Penduduk asli yang pertama yang telah menurunkan generasi baru. Jadi buah kelapa/Nyiur yang tumbuh itu mengandung kiasan bahwa tiap Pramuka merupakan inti bagi kelangsungan hidup bangsa Indonesia.</li>
						<li>Buah Kelapa juga dapat bertahan lama dalam keadaan seperti apapun dan bagaimanapun juga, jadi lambang tersebut mengkiaskan bahwa tiap pramuka adalah seorang Rohaniah dan jasmani sehat, kuat, ulet, serta besar tekadnya dalam menghadapi segala tantangan dalam kehidupan ini dan menempuh segala ujian yang diberikan kepada mereka, dan menyelesaikan segala tantangan dalam kehidupan untuk mengabdi kepada bangsa dan tanah air.</li>
						<li>Kelapa dapat tumbuh dimana saja, inilah yang membuktikan besarnya dalam upaya menyesuaikan dirinya dengan keadaan sekelilingnya. jadi melambangkan, bahwa tiap anggota pramuka dapat menyesuaikan dirinya dalam kehidupan bermasyarakat dimana dia berada dan dalam keadaan bagaimanapun juga.</li>
						<li>Kelapa tumbuh menjulang lurus tinggi keatas dan merupakan salah satu pohon tertinggi yang ada di indonesia, yang melambangkan bahwa tiap anggota pramuka memiliki Cita-cita yang tinggi dan lurus yakni mulia dan jujur, dan ia tetap tegak tidak mudah terombang-ambingkan oleh sesuatu.</li>
						<li>Akar kelapa tumbuh kuat dan erat dalam tanah. Jadi lambang itu mengkiaskan, tekad dan keyakinan pramuka yang berpegang pada dasar-dasar dan landasan-landasan yang baik, benar, kuat dan nyata ialah tekad yang digunakannya untuk memperkuat dirinya dalam menggapai sebuah cita-cita</li>
						<li>Kelapa adalah pohon yang serba guna, dari ujung akar hingga ujung daunnya memiliki manfaat. jadi lambang itu mengkiaskan, bahwa tiap pramuka adalah manusia yang berguna, dan membuktikan diri dan kegunaannya kepada kepentingan tanah air, bangsa dan negara Republik Indonesia.</li>
