	<font face="franklin Gothic" size="5"><center><b>Sejarah Pramuka di Dunia</b></center></font></center><br>
	<P><font size="3">ROBERT  STEVENSON SMYTH LORD BADEN POWELL OF GILWELL lahir pada tanggal 22 Februari 1857 di London. Tahun 1910 Baden Powell minta pensiun dari Tentara dengan pangkat terakhir Letnan Jendral. Baden Powell menikah dengan OLAVEST CLAIR SOAMES pada tahun 1912. Baden Powell meninggal 8 January 1941 di Anyeri, Kenya, Afrika.<br>
	<p>Pada awal tahun 1908 Baden powell selalu menulis cerita pengalamanya sabagai bungkus acara latihan kepramukaan yang dirintisnya.kumpulan tulisanya itukemudian terbit sebagai buku SCOUTING FOR BOYS buku ini cepat tersebar keseluruh Negeri Inggris.bahkan di Negara-negara lainya dan berdirilah di mana-manaorganisasi Kepramukaan yang semula hanya untuk anak laki-laki berusia Penggalang yang di sebut BOYS SCOUT.<br>
	<p>Kemudian disusul berdirinya organisasi kepramukaan putri yang di beri nama GIRL GUIDES atas bantuan adik Perempuan nya yang bernama AGNES dan kemudianditeruskan oleh Nyonya Baden powell.Tahun 1916 berdiri kelompok pramuka usia Siaga yangdisebut CUB (anak serigala) dengan buku THE JUNGGLEBOOK berisi cerita tentang MOWGLI anak didikan rimba(anak yang dipelihara dihutan oleh induk serigala) karangan RUDYARD KIPLING sebagai ceritera pembungkus Cub tersebut.<br>
	<p>Tahun1918 Baden powell membentuk ROVER SCOUT pramuka usia Penegak untuk menampung mereka yangsudah lewat usia 17 tahun,tetapi masih senang giat dibidang kepramukaan.tahun 1922 Baden powell menerbitkan buku ROVERING TO SUCCESS mengembaramenuju bahagia yang berisi petunjuk bagi para pramuka penegak dalam menghadapi hidupnya,agar mencapai kebahagiaan.<br>
	<p>Tahun 1920 diselenggarakan Jambore Sedunia di arenaOlimpiade London.Baden powell telah mengundang pramuka dari 27 Negara dan pada saat itu Baden powell diangkat sebagai Bapak Pandu Sedunia (CIEF SCOUT OF THE WORLD).</font>
		

<br><br><br><br><br>
	<p><font face="franklin Gothic" size="5"><center><b>Sejarah Pramuka di Indonesia</b></center></font></center><br>
	<p><font size="3">Gerakan Pramuka di Indonesia merupakan bagian dari sejarah perjuangan kemerdekaan Indonesia.Gerakan Pramuka di Indonesia diawali dengan didirikannya gerakan Kepanduan penjajah Belanda di Indonesia yang bernama NIPV (Nederland Indische Padvinders Vereeniging = Persatuan Pandu-Pandu Hindia Belanda).<br>
	<p>Pada saat yang sama, para pejuang kemerdekaan Indonesia ikut mendirikan gerakan kepanduan yang bertujuan membentuk manusia Indonesia yang baik dan menjadi kader pergerakan Nasional. Kemudian muncul bermacam-macam organisasi Kepanduan,antara lain JPO (Javaanse Padvinders Organizatie), JJP (Jong Java Padvindery), SIAP (Sarekat Islam Afdeling Padvinders), HW (Hisbul Wathon) dan lain-lain.<br>
	<p>Pada akhirnya Belanda mencurigai gerakan-gerakan Kepanduantersebut, sehingga Belanda melarang gerakan Kepanduan yang menggunakan istilah Padvinders.Dengan adanya larangan tersebut, K.H. Agus Salim menggunakan namaPandu atau Kepanduan untuk mengganti nama Padvinders. Setelah itu, organisasi Kepanduan Indonesia yang menggunakan istilah Padvindersmerubahnya menjadi Pandu atau Kepanduan, seperti  KBI (Kepanduan Bangsa Indonesia), BPPKI (Badan Pusat Persaudaraan Kepanduan Indonesia), dan lain-lain.<br>
	<p>Nama Pandu masih digunakan hingga masa kemedekaan.Karena banyaknya gerakan Kepanduan di Indonesia, presiden Sukarno menyatukan Gerakan Kepanduan tersebut dalam satu wadah, yaitu Gerakan Pramuka. Keputusan ini ditetapkan melalui Kepres No. 238 Tahun 1961 tentang Gerakan Pramuka yang pada tanggal 20 Mei 1961 ditandatangani oleh Pjs. Presiden RI Ir. Juanda, karena Presiden Soekarno sedang berkunjung ke Jepang.Di dalam Keppres ini Gerakan Pramuka oleh pemerintah ditetapkan sebagai satu-satunya badan di wilayah Indonesia yang diperkenankan menyelenggarakan pendidikan Kepramukaan, sehingga organisasi lain yang menyerupai dan sama sifatnya dengan Gerakan Pramuka dilarang keberadaannya.<br>


	<p>Sebagaimana disebutkan di atas, bahwa Bapak Pramuka Dunia adalah Baden Powell, orang yang menjadi perintis gerakan pramuka sedunia. Sedangkan Bapak Pramuka Indonesia adalah Sri Sultan Hamengkubuwono IX (lahir di Sompilan Ngasem, Yogyakarta, 12 April 1912, dan wafat di  Washington DC, Amerika Serikat, 1 Oktober 1988 ).Beliau adalah seorang Raja Kasultanan Yogyakarta dan Gubernur Daerah Istimewa Yogyakarta. Beliau juga Wakil Presiden Republik Indonesia yang kedua antara tahun 1973 s.d 1978.Beliau juga dikenal sebagai Bapak Pramuka Indonesia, dan pernah menjabat sebagai Ketua Kwartir Nasional Gerakan Pramuka (1961 s.d. 1974).
